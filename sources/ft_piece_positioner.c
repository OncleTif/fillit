#include "fillit.h"

void	ft_piece_positioner(t_piece *p)
{
	int	i;

	i = 1;
	while (i < 4)
	{
		p->sq[i][0] = p->sq[i][0] - p->sq[0][0];
		p->sq[i][1] = p->sq[i][1] - p->sq[0][1];
		i++;
	}
	p->sq[0][0] = 0;
	p->sq[0][1] = 0;
}
